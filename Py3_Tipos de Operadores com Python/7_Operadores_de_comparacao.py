#igual ou diferente
saldo = 200
saque = 100

print("Saldo é igual == estoque?", saldo == saque)
print("Saldo é diferente != de estoque?", saldo != saque)

#maior ou igual

print("saldo maior que saque(>)", saldo > saque)
print("saldo menor que saque(<)", saldo < saque)
print("saldo maior ou igual(>=)", saldo >= saque)
print("saldo menor ou igual(<=)", saldo <= saque)